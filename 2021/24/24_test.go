package main

import (
	"os"
	"testing"
)

func TestExampleTwentyFourOne(t *testing.T) {
	file, _ := os.ReadFile("./input")
	input := string(file)
	tests := []struct {
		expected int
	}{
		{13579246899999},
	}

	for _, test := range tests {
		result := run(input)
		if test.expected != result {
			t.Fatalf("Result % d != expected % d", result, test.expected)
		}
	}
}

